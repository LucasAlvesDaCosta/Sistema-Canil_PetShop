    private void btnCachorroCadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCachorroCadActionPerformed
        int idade = 0;
        boolean idadecerta = false;
        if(txtNomeCachorroCad.getText().trim().equals("") || txtRacaCachorroCad.getText().trim().equals("")
                || txtIdadeCachorroCad.getText().trim().equals("") || txtCorCachorroCad.getText().trim().equals("")){
            JOptionPane.showMessageDialog(this, "Preencha todos os campos!", "ERRO!", JOptionPane.ERROR_MESSAGE);
        }
        else{
            try{
                idade = Integer.parseInt(txtIdadeCachorroCad.getText());
                idadecerta = true;
            } catch (NumberFormatException e){
                JOptionPane.showMessageDialog(this, "Digite uma Idade Inteira!", "Erro de Idade!", JOptionPane.ERROR_MESSAGE);
                txtIdadeCachorroCad.setText("");
            }
        }
        
        if(idadecerta){
            Cachorro c = new Cachorro();
            c.setNome(txtNomeCachorroCad.getText());
            c.setRaca(txtRacaCachorroCad.getText());
            c.setCor(txtCorCachorroCad.getText());
            c.setIdade(idade);
            lstc.add(c);
            JOptionPane.showMessageDialog(this, "Cachorro Cadastrado com Sucesso!", "Sucesso!", JOptionPane.INFORMATION_MESSAGE);
            txtNomeCachorroCad.setText("");
            txtRacaCachorroCad.setText("");
            txtIdadeCachorroCad.setText("");
            txtCorCachorroCad.setText("");
            jfp.recarregarCachorro(lstc.size()-1);   
        }
        
    }//GEN-LAST:event_btnCachorroCadActionPerformed

